class Solution {
 public:
  int countTriplets(vector<int>& arr) {
    int n = arr.size();
    int ans = 0;
    for (int i = 0; i < n - 1; ++i) {
      int a = arr[i];
      for (int j = i + 1; j < n; ++j) {
        a ^= arr[j];
        if (a == 0) {
          ans += j - i;
        }
      }
    }
    return ans;
  }
};

